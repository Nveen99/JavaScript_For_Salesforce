console.log("Data Types");

let myName = "Naveen Kumar";
console.log("myName:" + myName);
console.log("myName:" + typeof myName); //string data type

myName = 23;
console.log("myName:" + myName);
console.log("myName:" + typeof myName); //number

myName = {};
console.log("myName:" + myName);
console.log("myName:" + typeof myName); //object

myName = [];
console.log("myName:" + myName);
console.log("myName:" + typeof myName); //object

myName = null;
console.log("myName:" + myName);
console.log("myName:" + typeof myName); //object

myName;
console.log("myName:" + myName);
console.log("myName:" + typeof myName); //undefined

myName = true;
console.log("myName:" + myName);
console.log("myName:" + typeof myName); //boolean
